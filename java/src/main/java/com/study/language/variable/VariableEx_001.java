package com.study.language.variable;

public class VariableEx_001 {

    public static void main(String[] args) {
        int year = 0;
        int age = 14;

        System.out.println(year);
        System.out.println(age);

        year = age + 2022;
        age = age + 1;

        System.out.println(year);
        System.out.println(age);
    }
}
